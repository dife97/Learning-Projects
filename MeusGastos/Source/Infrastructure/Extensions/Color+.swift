import UIKit

extension UIColor {
    static var inputBlue: UIColor {
        return UIColor(named: "inputBlue") ?? UIColor.blue
    }
    
    static var outputRed: UIColor {
        return UIColor(named: "outputRed") ?? UIColor.red
    }
    
    static var iconDarkGrey: UIColor {
        return UIColor(named: "iconDarkGrey") ?? UIColor.darkGray
    }
}
